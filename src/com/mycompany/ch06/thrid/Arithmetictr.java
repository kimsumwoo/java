package com.mycompany.ch06.thrid;

public class Arithmetictr {
	int sum = 0;
public int add(int a, int b) {
	sum = a + b;
	return sum;
}
public int min(int c, int d) {
	sum = c - d;
	return sum;
}
public int mul(int e, int f) {
	sum = e * f;
	return sum;
}
public int div(int g, int h) {
	sum = g / h;
	return sum;
}
public int rem(int i, int j) {
	sum = i % j;
	return sum;
}

}
