public class Arithmetic2 {
	int sum = 0;
	
	public  int num(int a, int b) {
		sum = a+b;
		return sum;
	}
	public int num1(int c, int d) {
		sum = c-d;
		return sum;
	}
	public int num3(int e, int f) {
		sum= e*f;
		return sum;
	}
	public int num4(int g, int h) {
		sum= g/h;
		return sum;
	}
	public int num5(int i, int j) {
		sum= i%j;
		return sum;
	}
	

}
